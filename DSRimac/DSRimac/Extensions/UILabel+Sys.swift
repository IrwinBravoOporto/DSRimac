    func setLineHeight(lineHeight: CGFloat, textSpacing: Double? = nil, isUnderline : Bool? = nil) {
        let text = self.text
        
        if let text = text {
            let paragraphStyle = NSMutableParagraphStyle()
//            let range = NSString(string: text).range(of: text)
            self.lineBreakMode = .byWordWrapping
            paragraphStyle.lineHeightMultiple = lineHeight
            self.attributedText = NSMutableAttributedString(string: text, attributes: [NSAttributedString.Key.paragraphStyle : paragraphStyle,NSAttributedString.Key.kern : textSpacing ?? 0 ])
            
//            attributeString.addAttribute(.paragraphStyle, value: paragraphStyle, range: range)
//            self.attributedText = attributeString
        }
    }
    
    func setTextSpacingBy(value: Double) {
       if let textString = self.text {
         let attributedString = NSMutableAttributedString(string: textString)
           attributedString.addAttribute(NSAttributedString.Key.kern, value: value, range: NSRange(location: 0, length: attributedString.length - 1))
         attributedText = attributedString
       }
     }
